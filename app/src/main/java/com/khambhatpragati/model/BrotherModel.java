package com.khambhatpragati.model;

public class BrotherModel {
    public String record_no;
    public String parent_member_id;
    public String name;
    public String date_of_birth;
    public String mobile_no;
    public String brother_id;

    public String getRecord_no() {
        return record_no;
    }

    public void setRecord_no(String record_no) {
        this.record_no = record_no;
    }

    public String getParent_member_id() {
        return parent_member_id;
    }

    public void setParent_member_id(String parent_member_id) {
        this.parent_member_id = parent_member_id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDate_of_birth() {
        return date_of_birth;
    }

    public void setDate_of_birth(String date_of_birth) {
        this.date_of_birth = date_of_birth;
    }

    public String getMobile_no() {
        return mobile_no;
    }

    public void setMobile_no(String mobile_no) {
        this.mobile_no = mobile_no;
    }

    public String getBrother_id() {
        return brother_id;
    }

    public void setBrother_id(String brother_id) {
        this.brother_id = brother_id;
    }
}
